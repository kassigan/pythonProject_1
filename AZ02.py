import pandas as pd
import numpy as np

# 1️⃣ DataFrame с оценками 10 учеников по 5 предметам
data = {
    'Имя': ['Аня', 'Борис', 'Вика', 'Глеб', 'Даша', 'Егор', 'Женя', 'Ира', 'Кирилл', 'Лена'],
    'Математика': [5, 4, 3, 4, 5, 5, 3, 4, 5, 4],
    'Русский язык': [4, 5, 4, 3, 5, 4, 3, 5, 5, 4],
    'История': [3, 4, 4, 5, 5, 3, 4, 5, 4, 3],
    'Физика': [5, 4, 3, 5, 5, 4, 4, 5, 3, 4],
    'Английский': [4, 3, 5, 5, 4, 5, 3, 4, 4, 5]
}

df = pd.DataFrame(data)

# 2️⃣ Выведем первые несколько строк
print("Первые строки таблицы:")
print(df.head(), "\n")

# 3️⃣ Средняя оценка по каждому предмету (кроме столбца 'Имя')
avg_scores = df.drop('Имя', axis=1).mean()
print("Средняя оценка по предметам:")
print(avg_scores, "\n")

# 4️⃣ Медианная оценка по каждому предмету
median_scores = df.drop('Имя', axis=1).median()
print("Медианная оценка по предметам:")
print(median_scores, "\n")

# 5️⃣ Q1, Q3 и IQR для Математики
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"Q1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")
print(f"IQR по математике: {IQR_math}\n")

# 6️⃣ Стандартное отклонение по каждому предмету
std_scores = df.drop('Имя', axis=1).std()
print("Стандартное отклонение по предметам:")
print(std_scores)


